package com.gp.generic;

public class MyGenericClass2<K, V> {
	private K key;
	private V value;

	public MyGenericClass2(K key, V value) {
		super();
		this.key = key;
		this.value = value;
	}

	public K getKey() {
		return key;
	}

	public void setKey(K key) {
		this.key = key;
	}

	public V getValue() {
		return value;
	}

	public void setValue(V value) {
		this.value = value;
	}

}
